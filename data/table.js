export const reformatData = listData => {
  const copyData = _.cloneDeep(listData)
  copyData.forEach((data, i) => {
    data['No'] = i+1
    data['Nama'] = data.name?.toUpperCase()
    data['Tempat Lahir'] = data.pob?.toUpperCase()
    data['Tanggal Lahir'] = moment(data.dob, 'MM-DD-YY').format('DD MMMM YYYY').toUpperCase()
    data['Alamat'] = data.address?.toUpperCase()
    data['Tanggal Periksa'] = moment(data.checkupDate, 'MM-DD-YY').format('DD MMMM YYYY').toUpperCase()
    data['Hasil'] = data.result?.toUpperCase()
    delete data.key; delete data.name; delete data.pob; delete data.dob; delete data.address; delete data.checkupDate; delete data.result;
    delete data.time
  })

  return copyData
}
